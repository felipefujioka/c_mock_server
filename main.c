#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <sys/types.h>
#include <sys/socket.h>
#include <netinet/in.h>

#define BUFFER_SIZE 2048

void error(const char *msg) {
  perror(msg);
  exit(1);
}

int main(int argc, char *argv[]) {
  int sockfd, newsockfd, portno;
  socklen_t clilen;
  char buffer[BUFFER_SIZE];
  struct sockaddr_in serv_addr, cli_addr;
  int n;

  char fix_logon_answer[] = {
    0x38, 0x3d, 0x46, 0x49, 0x58, 0x2e, 0x34, 0x2e,
    0x32, 0x01, 0x39, 0x3d, 0x37, 0x39, 0x01, 0x33,
    0x35, 0x3d, 0x41, 0x01, 0x33, 0x34, 0x3d, 0x31,
    0x01, 0x34, 0x39, 0x3d, 0x41, 0x43, 0x43, 0x45,
    0x50, 0x54, 0x4f, 0x52, 0x01, 0x35, 0x32, 0x3d,
    0x32, 0x30, 0x31, 0x37, 0x30, 0x32, 0x31, 0x33,
    0x2d, 0x32, 0x30, 0x3a, 0x35, 0x36, 0x3a, 0x30,
    0x37, 0x2e, 0x38, 0x31, 0x30, 0x01, 0x35, 0x36,
    0x3d, 0x49, 0x4e, 0x49, 0x54, 0x49, 0x41, 0x54,
    0x4f, 0x52, 0x01, 0x39, 0x38, 0x3d, 0x30, 0x01,
    0x31, 0x30, 0x38, 0x3d, 0x36, 0x30, 0x30, 0x01,
    0x31, 0x34, 0x31, 0x3d, 0x59, 0x01, 0x31, 0x30,
    0x3d, 0x31, 0x30, 0x31, 0x01
  };

  char fix_subscription_answer[] = {
    0x38, 0x3d, 0x46, 0x49, 0x58, 0x2e, 0x34, 0x2e,
    0x32, 0x01, 0x39, 0x3d, 0x38, 0x37, 0x01, 0x33,
    0x35, 0x3d, 0x57, 0x01, 0x33, 0x34, 0x3d, 0x32,
    0x01, 0x34, 0x39, 0x3d, 0x41, 0x43, 0x43, 0x45,
    0x50, 0x54, 0x4f, 0x52, 0x01, 0x35, 0x32, 0x3d,
    0x32, 0x30, 0x31, 0x37, 0x30, 0x32, 0x31, 0x33,
    0x2d, 0x32, 0x30, 0x3a, 0x35, 0x36, 0x3a, 0x30,
    0x37, 0x2e, 0x38, 0x36, 0x38, 0x01, 0x35, 0x36,
    0x3d, 0x49, 0x4e, 0x49, 0x54, 0x49, 0x41, 0x54,
    0x4f, 0x52, 0x01, 0x32, 0x32, 0x3d, 0x38, 0x01,
    0x34, 0x38, 0x3d, 0x01, 0x35, 0x35, 0x3d, 0x01,
    0x32, 0x30, 0x37, 0x3d, 0x42, 0x56, 0x4d, 0x46,
    0x01, 0x32, 0x36, 0x32, 0x3d, 0x01, 0x31, 0x30,
    0x3d, 0x30, 0x32, 0x31, 0x01
  };

  if (argc < 2) {
    fprintf(stderr,"ERROR, no port provided\n");
    exit(1);
  }

  sockfd = socket(AF_INET, SOCK_STREAM, 0);
  if (sockfd < 0) {
    error("ERROR opening socket");
  }

  bzero((char *) &serv_addr, sizeof(serv_addr));
  portno = atoi(argv[1]);
  serv_addr.sin_family = AF_INET;
  serv_addr.sin_addr.s_addr = INADDR_ANY;
  serv_addr.sin_port = htons(portno);

  if (bind(sockfd, (struct sockaddr *) &serv_addr, sizeof(serv_addr)) < 0) {
    error("ERROR on binding");
  }

  printf("Server listening on port %d\n", portno);

  listen(sockfd, 5);
  clilen = sizeof(cli_addr);

  newsockfd = accept(sockfd, (struct sockaddr *) &cli_addr, &clilen);
  if (newsockfd < 0) {
    error("ERROR on accept");
  }

  bzero(buffer, BUFFER_SIZE);
  n = read(newsockfd, buffer, BUFFER_SIZE);
  if (n < 0) {
    error("ERROR reading from socket");
  }

  printf("Received: %s\n", buffer);
  printf("Sending back: %s\n", fix_logon_answer);

  n = write(newsockfd, fix_logon_answer, strlen(fix_logon_answer));
  if (n < 0) {
    error("ERROR writing to socket");
  }
  /* Established FIX session, messages will now be exchanged via newsockfd */

  bzero(buffer, BUFFER_SIZE);
  n = read(newsockfd, buffer, BUFFER_SIZE);
  if (n < 0) {
    error("ERROR reading from socket");
  }

  printf("Received: %s\n", buffer);
  printf("Sending back: %s\n", fix_subscription_answer);

  n = write(newsockfd, fix_subscription_answer, strlen(fix_subscription_answer));
  if (n < 0) {
    error("ERROR writing to socket");
  }

  close(newsockfd);
  close(sockfd);

  return 0;
}
